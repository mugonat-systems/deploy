<?php

namespace Deployer;

desc('NPM install & build for production');
task('deploy:node_modules', function () {
    cd('{{release_or_current_path}}');
    run('npm install');
    run('npm run build');
});

set('hook_node_modules', true);

if (get('hook_node_modules')) {
    after('deploy:vendors', 'deploy:node_modules');
}
